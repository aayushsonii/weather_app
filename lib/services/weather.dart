import 'package:climaa/services/location.dart';
import 'package:climaa/services/networking.dart';
import 'package:climaa/utilities/constants.dart';

class WeatherModel {
  Future<dynamic> getLocationweather() async {
    GetLocation getLocation = GetLocation();
    await getLocation.getCurrentLocation();
    print('getlocdata');
    NetworkHelper networkHelper = NetworkHelper(
        url:
            '${openWeatherDataURL}lat=${getLocation.latitude}&lon=${getLocation.longitude}&APPID=$appId&units=metric');

    var weatherData = await networkHelper.getData();
    return weatherData;
  }

  String getWeatherIcon(int condition) {
    if (condition < 300) {
      return '🌩';
    } else if (condition < 400) {
      return '🌧';
    } else if (condition < 600) {
      return '☔️';
    } else if (condition < 700) {
      return '☃️';
    } else if (condition < 800) {
      return '🌫';
    } else if (condition == 800) {
      return '☀️';
    } else if (condition <= 804) {
      return '☁️';
    } else {
      return '🤷‍';
    }
  }

  String getMessage(int temp) {
    if (temp > 25) {
      return 'It\'s 🍦 time';
    } else if (temp > 20) {
      return 'Time for shorts and 👕';
    } else if (temp < 10) {
      return 'You\'ll need 🧣 and 🧤';
    } else {
      return 'Bring a 🧥 just in case';
    }
  }
}
